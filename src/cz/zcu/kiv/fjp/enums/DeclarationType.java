package cz.zcu.kiv.fjp.enums;

/**
 * Enum values representing declaration types
 * These types can be:
 * label - declaration of a label
 * procedure - declaration of a procedure
 * variable - declaration of variable(s)
 * variable parallel - declaration of a parallel variables
 * constant - declaration of constant(s)
 */
public enum DeclarationType {

    LABEL,
    PROCEDURE,
    VARIABLE,
    VARIABLE_PARALLEL,
    CONSTANT
}
